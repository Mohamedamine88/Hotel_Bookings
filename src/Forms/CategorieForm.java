    private void SupprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SupprimerActionPerformed
   if (id != 0) {
        Categorie categorie = cats.findById(id);
        if (categorie != null) {
            int confirm = JOptionPane.showConfirmDialog(this, "Etes-vous sûres de vouloir supprimer cette catégorie ?", "Confirmation", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                if (cats.delete(categorie)) {
                    JOptionPane.showMessageDialog(this, "Categorie supprimée avec succès !");
                    load();
                } else {
                    JOptionPane.showMessageDialog(this, "Erreur de suppression de la catégorie.");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Catégorie non trouvée.");
        }
    } else {
        JOptionPane.showMessageDialog(this, "Veuillez selectionner la catégorie à supprimer.");
    }
    }//GEN-LAST:event_SupprimerActionPerformed


