    private void ModifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModifierActionPerformed
if (id != 0) {
            String code = codetxt.getText();
            String libelle = libelletxt.getText();
            Categorie categorie = new Categorie(id, code, libelle);
            if (cats.update(categorie)) {
                JOptionPane.showMessageDialog(this, "Catégorie mise à jour avec succès !");
                load();
            } else {
                JOptionPane.showMessageDialog(this, "Erreur de mise à jour de la catégorie.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Veuillez choisir la catégorie à mettre à jour.");
        }
    }//GEN-LAST:event_ModifierActionPerformed

